# --- –§—É–Ω–∫—Ü–∏—è –¥–ª—è –°–¢–†–ò–ú–ò–ù–ì–ê –æ—Ç–≤–µ—Ç–∞ API OpenRouter ---
# –≠—Ç–∞ —Ñ—É–Ω–∫—Ü–∏—è —è–≤–ª—è–µ—Ç—Å—è –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä–æ–º (–∏—Å–ø–æ–ª—å–∑—É–µ—Ç yield)
def stream_ai_response(model_id_func, chat_history_func):
    """–ó–∞–ø—Ä–∞—à–∏–≤–∞–µ—Ç –æ—Ç–≤–µ—Ç —É API –≤ —Ä–µ–∂–∏–º–µ —Å—Ç—Ä–∏–º–∏–Ω–≥–∞ –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∫—É—Å–æ—á–∫–∏ —Ç–µ–∫—Å—Ç–∞."""
    # --- –ü–û–õ–£–ß–ï–ù–ò–ï –ö–õ–Æ–ß–ê –ò–ó –°–ï–ö–†–ï–¢–û–í ---
    try:
        api_key_from_secrets = st.secrets["OPENROUTER_API_KEY"]
    except KeyError:
        st.error("‚õî –°–µ–∫—Ä–µ—Ç 'OPENROUTER_API_KEY' –Ω–µ –Ω–∞–π–¥–µ–Ω. –£–±–µ–¥–∏—Ç–µ—Å—å, —á—Ç–æ –æ–Ω –¥–æ–±–∞–≤–ª–µ–Ω –≤ –Ω–∞—Å—Ç—Ä–æ–π–∫–∞—Ö –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è Streamlit Cloud.", icon="üö®")
        yield None # –ü—Ä–µ—Ä—ã–≤–∞–µ–º –≤—ã–ø–æ–ª–Ω–µ–Ω–∏–µ –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä–∞
        return # –í—ã—Ö–æ–¥–∏–º –∏–∑ —Ñ—É–Ω–∫—Ü–∏–∏
    except Exception as e:
        st.error(f"ü§Ø –û—à–∏–±–∫–∞ –ø—Ä–∏ –¥–æ—Å—Ç—É–ø–µ –∫ —Å–µ–∫—Ä–µ—Ç–∞–º: {e}", icon="üí•")
        yield None
        return
    # ------------------------------------

    headers = {
        "Authorization": f"Bearer {api_key_from_secrets}", # –ò—Å–ø–æ–ª—å–∑—É–µ–º –∫–ª—é—á –∏–∑ —Å–µ–∫—Ä–µ—Ç–æ–≤
        "Content-Type": "application/json",
    }
    payload = {
        "model": model_id_func,
        "messages": chat_history_func,
        "stream": True # –í–∫–ª—é—á–∞–µ–º —Ä–µ–∂–∏–º —Å—Ç—Ä–∏–º–∏–Ω–≥–∞
    }
    try:
        # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º POST-–∑–∞–ø—Ä–æ—Å —Å –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–º stream=True
        response = requests.post(OPENROUTER_API_URL, headers=headers, json=payload, stream=True, timeout=90)
        response.raise_for_status() # –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞ HTTP –æ—à–∏–±–∫–∏ (4xx, 5xx)

        # –ò—Ç–µ—Ä–∏—Ä—É–µ–º—Å—è –ø–æ —Å—Ç—Ä–æ–∫–∞–º –æ—Ç–≤–µ—Ç–∞ (Server-Sent Events)
        for line in response.iter_lines():
            if line: # –ü—Ä–æ–ø—É—Å–∫–∞–µ–º –ø—É—Å—Ç—ã–µ —Å—Ç—Ä–æ–∫–∏ (keep-alive)
                decoded_line = line.decode('utf-8') # –î–µ–∫–æ–¥–∏—Ä—É–µ–º —Å—Ç—Ä–æ–∫—É
                # –°–æ–±—ã—Ç–∏—è SSE –Ω–∞—á–∏–Ω–∞—é—Ç—Å—è —Å "data: "
                if decoded_line.startswith("data: "):
                    try:
                        # –£–±–∏—Ä–∞–µ–º –ø—Ä–µ—Ñ–∏–∫—Å "data: "
                        json_data = decoded_line[len("data: "):]
                        # –ü—Ä–æ–≤–µ—Ä—è–µ–º –º–∞—Ä–∫–µ—Ä –∫–æ–Ω—Ü–∞ –ø–æ—Ç–æ–∫–∞ "[DONE]"
                        if json_data.strip() == "[DONE]":
                            break # –ó–∞–≤–µ—Ä—à–∞–µ–º —Ü–∏–∫–ª, –µ—Å–ª–∏ –ø–æ—Ç–æ–∫ –∑–∞–∫–æ–Ω—á–µ–Ω
                        # –ü–∞—Ä—Å–∏–º JSON-—Å—Ç—Ä–æ–∫—É —Å –¥–∞–Ω–Ω—ã–º–∏ —á–∞–Ω–∫–∞
                        chunk = json.loads(json_data)
                        # –ò–∑–≤–ª–µ–∫–∞–µ–º –¥–µ–ª—å—Ç—É (–∏–∑–º–µ–Ω–µ–Ω–∏–µ) –∫–æ–Ω—Ç–µ–Ω—Ç–∞ –∏–∑ —á–∞–Ω–∫–∞
                        delta_content = chunk.get("choices", [{}])[0].get("delta", {}).get("content")
                        # –ï—Å–ª–∏ –≤ —á–∞–Ω–∫–µ –µ—Å—Ç—å —Ç–µ–∫—Å—Ç, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –µ–≥–æ
                        if delta_content:
                            yield delta_content # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –∫—É—Å–æ—á–µ–∫ —Ç–µ–∫—Å—Ç–∞
                    except json.JSONDecodeError:
                        # –û–±—Ä–∞–±–æ—Ç–∫–∞ —Å–ª—É—á–∞—è, –µ—Å–ª–∏ –ø—Ä–∏—à–µ–ª –Ω–µ–≤–∞–ª–∏–¥–Ω—ã–π JSON
                        st.warning(f"–ù–µ —É–¥–∞–ª–æ—Å—å –¥–µ–∫–æ–¥–∏—Ä–æ–≤–∞—Ç—å JSON –∏–∑ –ø–æ—Ç–æ–∫–∞: {json_data}", icon="‚ö†Ô∏è")
                        continue # –ü—Ä–æ–ø—É—Å–∫–∞–µ–º —ç—Ç—É —Å—Ç—Ä–æ–∫—É –∏ –∂–¥–µ–º —Å–ª–µ–¥—É—é—â—É—é
                    except Exception as e:
                        # –û–±—Ä–∞–±–æ—Ç–∫–∞ –¥—Ä—É–≥–∏—Ö –æ—à–∏–±–æ–∫ –ø—Ä–∏ –ø–∞—Ä—Å–∏–Ω–≥–µ —á–∞–Ω–∫–∞
                        st.error(f"–û—à–∏–±–∫–∞ –æ–±—Ä–∞–±–æ—Ç–∫–∏ —á–∞–Ω–∫–∞: {e} | –°—Ç—Ä–æ–∫–∞: {decoded_line}", icon="üí•")
                        break # –ü—Ä–µ—Ä—ã–≤–∞–µ–º –æ–±—Ä–∞–±–æ—Ç–∫—É –ø–æ—Ç–æ–∫–∞

    # –û–±—Ä–∞–±–æ—Ç–∫–∞ –æ—à–∏–±–æ–∫ –∑–∞–ø—Ä–æ—Å–∞ (—Ç–∞–π–º–∞—É—Ç, —Å–µ—Ç—å, API)
    except requests.exceptions.Timeout:
        st.error("‚è≥ –û—à–∏–±–∫–∞: –í—Ä–µ–º—è –æ–∂–∏–¥–∞–Ω–∏—è –æ—Ç–≤–µ—Ç–∞ –æ—Ç API –∏—Å—Ç–µ–∫–ª–æ.", icon="‚è±Ô∏è")
        yield None # –°–∏–≥–Ω–∞–ª–∏–∑–∏—Ä—É–µ–º –æ–± –æ—à–∏–±–∫–µ
    except requests.exceptions.RequestException as e:
        error_message = f"–û—à–∏–±–∫–∞ —Å–µ—Ç–∏ –∏–ª–∏ API OpenRouter: {e}"
        try:
            # –ü—ã—Ç–∞–µ–º—Å—è –ø–æ–ª—É—á–∏—Ç—å —Ç–µ–∫—Å—Ç –æ—à–∏–±–∫–∏ –∏–∑ –æ—Ç–≤–µ—Ç–∞ —Å–µ—Ä–≤–µ—Ä–∞
            error_details = e.response.text if e.response is not None else "–ù–µ—Ç –¥–µ—Ç–∞–ª–µ–π –æ—Ç–≤–µ—Ç–∞"
            error_message += f"\n–î–µ—Ç–∞–ª–∏: {error_details}"
        except:
             pass # –ï—Å–ª–∏ –Ω–µ —É–¥–∞–ª–æ—Å—å –ø–æ–ª—É—á–∏—Ç—å –¥–µ—Ç–∞–ª–∏, –Ω–∏—á–µ–≥–æ —Å—Ç—Ä–∞—à–Ω–æ–≥–æ
        st.error(error_message, icon="üåê")
        yield None # –°–∏–≥–Ω–∞–ª–∏–∑–∏—Ä—É–µ–º –æ–± –æ—à–∏–±–∫–µ
    except Exception as e:
        # –û–±—Ä–∞–±–æ—Ç–∫–∞ –¥—Ä—É–≥–∏—Ö –Ω–µ–ø—Ä–µ–¥–≤–∏–¥–µ–Ω–Ω—ã—Ö –æ—à–∏–±–æ–∫
        st.error(f"ü§Ø –ü—Ä–æ–∏–∑–æ—à–ª–∞ –Ω–µ–ø—Ä–µ–¥–≤–∏–¥–µ–Ω–Ω–∞—è –æ—à–∏–±–∫–∞ –ø—Ä–∏ —Å—Ç—Ä–∏–º–∏–Ω–≥–µ: {e}", icon="üí•")
        yield None # –°–∏–≥–Ω–∞–ª–∏–∑–∏—Ä—É–µ–º –æ–± –æ—à–∏–±–∫–µ

